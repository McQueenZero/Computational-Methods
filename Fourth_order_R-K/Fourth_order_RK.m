y = [1 0]; %1st column 初值 2nd column 序号
x = [0 0];
y_x = y; %解析解法初值
h = 0.1; %步长
for n = 1:5
    K1=f(x(n,1),y(n,1));
    K2=f(x(n,1)+h/2,y(n,1)+h/2*K1);
    K3=f(x(n,1)+h/2,y(n,1)+h/2*K2);
    K4=f(x(n,1)+h,y(n,1)+h*K3);
    x(n+1,1)=x(n,1)+h;
    y(n+1,1)=y(n,1)+h/6*(K1+2*K2+2*K3+K4);
    y_x(n+1,1)=yx(x(n+1,1));
    y(n+1,2)=n; %标序号
    x(n+1,2)=n; %标序号
    y_x(n+1,2)=n; %标序号
end
LB = zeros(n+1,5); %列表输出
LB(:,1)=x(:,1); %x
LB(:,2)=y(:,1); %yn
LB(:,3)=y_x(:,1); %yx
LB(:,4)=abs(LB(:,3)-LB(:,2)); %解析数值解与方法解之差
LB(:,5)=y(:,2); %序号